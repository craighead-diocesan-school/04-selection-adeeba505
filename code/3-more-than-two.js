function moreThanTwo() {
  // write your code here
  let digit = Number(prompt('What number do you wanna compare to 2?'))

  if (digit == 2) {
    alert ('2 is equal to 2')
  }
  else if (digit > 2) {
    alert (digit +' is bigger than 2')
  }
  else if (digit < 2) {
    alert (digit +' is smaller than 2')
  }
}
